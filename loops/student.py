student_percentage = float(input("Enter The percentage of the Student"))
print("The Performance of student is : ")

if student_percentage>= 90 :
    print("Excellent ")
elif student_percentage >= 80 :
    print("Very Good  ")
elif student_percentage >= 70 :
    print(" Good  ")
elif student_percentage >= 60 :
    print(" Average  ")
else :
    print("Poor ")

